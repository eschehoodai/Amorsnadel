// ===== Engine =====
function getCurrentLang() {
  return localStorage.getItem('amorsnadel-lang') || 'de';
}

function setLang(lang) {
  localStorage.setItem('amorsnadel-lang', lang);
  applyTranslations(lang);
}

function toggleLang() {
  const current = getCurrentLang();
  setLang(current === 'de' ? 'ru' : 'de');
}

function applyTranslations(lang) {
  // Update <html lang>
  document.documentElement.lang = lang;

  // Update page title
  const page = detectPage();
  if (pageTitles[page]) {
    document.title = pageTitles[page][lang] || pageTitles[page].de;
  }

  // Text content via data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const t = translations[key];
    if (t) el.textContent = t[lang] || t.de;
  });

  // HTML content via data-i18n-html
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    const t = translations[key];
    if (t) el.innerHTML = t[lang] || t.de;
  });

  // Placeholders via data-i18n-ph
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const t = translations[key];
    if (t) el.placeholder = t[lang] || t.de;
  });

  // Update toggle button label
  document.querySelectorAll('.lang-toggle__label').forEach(el => {
    el.textContent = lang === 'de' ? 'RU' : 'DE';
  });

  // Update toggle button aria-label
  document.querySelectorAll('.lang-toggle').forEach(el => {
    el.setAttribute('aria-label', lang === 'de' ? 'Переключить на русский' : 'Auf Deutsch umschalten');
  });
}

function detectPage() {
  const path = window.location.pathname.toLowerCase();
  if (path.includes('galerie')) return 'galerie';
  if (path.includes('anfrage')) return 'anfrage';
  if (path.includes('kontakt')) return 'kontakt';
  return 'index';
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations(getCurrentLang());
});
